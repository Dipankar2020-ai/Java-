class Literal
{
	public static void main(String args[])
	{
		byte b1=10;
		byte b2=0b1010;
		byte b3=012;
		byte b4=0XA;
		System.out.println(b1);
		System.out.println(b2);
		System.out.println(b3);
		System.out.println(b4);
		float f1=1.24f;
		double d1=55.77;
		long l1=99999L;
		System.out.println(f1);
		System.out.println(d1);
		System.out.println(l1);


	}
}